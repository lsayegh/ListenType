function loadSong(){
	var artist = "The Killers";
	var song = "Mr. Brideside";
	
	document.getElementById('lblArtist').innerHTML = 'Artist: ' + artist;
	document.getElementById('lblSong').innerHTML = 'Song: ' + song;

	var node = document.createElement('label');
	node.innerHTML = '</br>';
	document.getElementById('container').appendChild(node);
	
	for (var i = 0; i < lyric.length; i++) {
		var matrix = lyric[i];
		for (var j = 0; j < matrix.length; j++) {
			var node = document.createElement('label');
			var word = matrix[j];
			var word2 = Array(word.length+1).join('*')
			node.innerHTML = '<label id="lbl' + i + j + '">' + word2 + '&nbsp</label>';
			document.getElementById('container').appendChild(node);
		}
		
		var node = document.createElement('label');
		node.innerHTML = '</br>';
		document.getElementById('container').appendChild(node);
	
	}
}

function guessWord(guess) {
	if(guess.length > 0) {
			var entryArray = guess.split(" ");
			for (var x = 0; x < entryArray.length; x++) {
				for (var i =  0; i < lyric.length; i++) {
					var matrix = lyric[i];
					for (var j = 0; j < matrix.length; j++) {
							var word = matrix[j];
							if (entryArray[x].toLowerCase() == word.toLowerCase()) {
									document.getElementById('lbl'+i+j).innerHTML = word + '&nbsp';
									document.getElementById('lbl'+i+j).style.color = "blue";
									
							}
					}
				}
			}
			document.getElementById('txtEnter').value = ""	
	}
}


